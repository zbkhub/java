package com.qf.mode;

public class Users {
	private int id;
	private int pricid;
	private String username;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getPricid() {
		return pricid;
	}
	public void setPricid(int pricid) {
		this.pricid = pricid;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	
}
